        if (NombreATextField.getText() == "" || RazaTextField.getText() == "") {
            JOptionPane.showMessageDialog(null, "¬°ERROR! Debes rellenar todos los campos");
        } else {
            try {
                Conexion c = new Conexion("servidor.choto.es", "3306", "alu_Juanma_zoo1", "unalumno", "soyunalumno2022");
                Connection conn = c.makeConnect();
                String nombre = NombreATextField.getText();
                String raza = RazaTextField.getText();
                String genero = ComboBoxGenero.getSelectedItem().toString();
                Statement st = conn.createStatement();
                String sql = "INSERT INTO animales VALUES (null,'" + nombre + "','" + raza + "','" + genero + "',current_timestamp())";
                int resultado = st.executeUpdate(sql);

                if (resultado > 0) {

                    sql = "SELECT * FROM animales";
                    ResultSet rs = st.executeQuery(sql);
                    DefaultTableModel tabla = (DefaultTableModel) TablaAnimales.getModel();

                    while (tabla.getRowCount() > 0) {
                        tabla.removeRow(0);
                    }

                    while (rs.next()) {

                        tabla.addRow(new Object[]{rs.getInt("id"), rs.getString("nombre"), rs.getString("raza"),
                            rs.getString("genero")});

                    }
                    JOptionPane.showMessageDialog(null, "Animal registrado correctamente");
                    jDialogAddAnimal.dispose();

                } else {

                    JOptionPane.showMessageDialog(null, "Ha habido un error registrando al animal, prueba de nuevo");
                    NombreATextField.setText("");
                    RazaTextField.setText("");

                }

            } catch (SQLException ex) {
                Logger.getLogger(Animales.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
